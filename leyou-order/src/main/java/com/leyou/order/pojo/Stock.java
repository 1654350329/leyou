package com.leyou.order.pojo;

public class Stock {
    private int sku_id;
    private int seckill_stock;
    private int seckill_totalint;
    private int stock;

    public int getSku_id() {
        return sku_id;
    }

    public void setSku_id(int sku_id) {
        this.sku_id = sku_id;
    }

    public int getSeckill_stock() {
        return seckill_stock;
    }

    public void setSeckill_stock(int seckill_stock) {
        this.seckill_stock = seckill_stock;
    }

    public int getSeckill_totalint() {
        return seckill_totalint;
    }

    public void setSeckill_totalint(int seckill_totalint) {
        this.seckill_totalint = seckill_totalint;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
}
